
"""
Problem 36 「二種類の基数による回文数」

585 = 10010010012 (2進) は10進でも2進でも回文数である.

100万未満で10進でも2進でも回文数になるような数の総和を求めよ.

(注: 先頭に0を含めて回文にすることは許されない.)

"""

kaibun = []

for i in range(1, 1000000):
    j = (bin(i)[2:])
    if str(i) == str(i)[::-1] and str(j) == str(j)[::-1]:
        kaibun.append(i)

print(kaibun)
print(sum(kaibun))

